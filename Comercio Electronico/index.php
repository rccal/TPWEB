        <h3>¿Que es el Comercio Electronico?</h3>
        <p>El comercio electrónico (conocido popularmente como ecommerce), es la compraventa y distribución de bienes y servicios a través de internet u otras redes informáticas.
            Constituye un nuevo soporte para la actividad comercial porque carece de materialidad y de pautas físicas (espacio y tiempo) pero le da accesibilidad y rapidez. De tal modo que se pueden realizar transacciones todos los días del año a cualquier hora y en cualquier lugar del mundo.</p>
